package com.designpattern;

/**
 * The Music class performs several tasks as shown in the methods.
 *
 * @Author: Bridget
 */
public class Music {
    /**
     * turn on music
     */
    public void on() {
        System.out.println("Turn on the music");
    }

    /**
     * turn off music
     */
    public void off() {
        System.out.println("Turn off the music");
    }
}

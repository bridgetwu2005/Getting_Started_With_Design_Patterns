package com.designpattern;

/**
 * The Light class performs several tasks as shown in the methods.
 *
 * @Author: Bridget
 */
public class Light {
    /**
     * turn on light
     */
    public void on() {
        System.out.println("Turn on the light");
    }

    /**
     * turn off light
     */
    public void off() {
        System.out.println("Turn off the light");
    }
}
